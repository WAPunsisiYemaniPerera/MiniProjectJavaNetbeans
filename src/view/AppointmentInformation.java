    private void jbtnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnAddActionPerformed
        // Check if any of the fields are empty
    if(jtxtAppointmentNo.getText().isEmpty() || jtxtPetID.getText().isEmpty() ||
       jtxtPetName.getText().isEmpty() || jtxtOwnerName.getText().isEmpty() ||
       jtxtTelephone.getText().isEmpty() || jtxtDate.getText().isEmpty() ||jtxtTime.getText().isEmpty()
       ) {
        JOptionPane.showMessageDialog(this, "Please fill in all fields.");
        return; // Exit the method if any field is empty
    }
    
    // If all fields are filled, proceed to add the appointment
    appointmentController.addAppointment(jtxtAppointmentNo.getText(), jtxtPetID.getText(), jtxtPetName.getText(),
            jtxtOwnerName.getText(), jtxtTelephone.getText(), jtxtDate.getText(),jtxtTime.getText());
    JOptionPane.showMessageDialog(this, "Record Added");
    updateTable();
    setNextAppointmentNo();
    resetFields();
        
    }//GEN-LAST:event_jbtnAddActionPerformed
    


    
    private void jbtnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnUpdateActionPerformed
         DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();
    int selectedRow = jTable1.getSelectedRow();

    if (selectedRow == -1) { // Check if no row is selected
        JOptionPane.showMessageDialog(this, "Please select a row to update.");
        return; // Exit the method
    }

    int id = Integer.parseInt(tableModel.getValueAt(selectedRow, 0).toString());

    appointmentController.updateAppointment(id, jtxtPetID.getText(), jtxtPetName.getText(), jtxtOwnerName.getText(),
            jtxtTelephone.getText(), jtxtDate.getText(), jtxtTime.getText());
    JOptionPane.showMessageDialog(this, "Record Updated");
    updateTable();
    setNextAppointmentNo();
    resetFields();
    }//GEN-LAST:event_jbtnUpdateActionPerformed

