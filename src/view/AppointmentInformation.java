    private void addPetIdListener() {
        jtxtPetID.addActionListener(evt -> fetchPetDetails());
    }

    private void fetchPetDetails() {
        Vector<String> petDetails = appointmentController.getPetDetails(jtxtPetID.getText());
        if (petDetails.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Pet ID not found");
        } else {
            jtxtPetName.setText(petDetails.get(0));
            jtxtOwnerName.setText(petDetails.get(1));
            jtxtTelephone.setText(petDetails.get(2));
        }
    }

    private void updateTable() {
        DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();
        tableModel.setRowCount(0);
        Vector<Vector<String>> data = appointmentController.getAllAppointments();
        for (Vector<String> row : data) {
            tableModel.addRow(row);
        }
    }
