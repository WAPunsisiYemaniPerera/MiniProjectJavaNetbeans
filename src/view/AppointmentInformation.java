    public void search() {
    String srch = searchbox.getText();
    try {
        String sql = "SELECT * FROM appointments WHERE PetName LIKE '%" + srch + "%' OR AppointmentNo LIKE '%" + srch + "%'";
        Connection conn = appointmentController.getConnections();
        pst = conn.prepareStatement(sql);
        rs = pst.executeQuery();
        jTable1.setModel(DbUtils.resultSetToTableModel(rs));
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, e);
    }
}
