    public void ItemCost() {
        double sum = 0;
        for (int i = 0; i < jTable1.getRowCount(); i++) {
            sum += Double.parseDouble(jTable1.getValueAt(i, 2).toString());
        }
        jtxtSubTotal.setText(Double.toString(sum));
        double cTotal1 = Double.parseDouble(jtxtSubTotal.getText());
        double cTax = (cTotal1 * 3.9) / 100;
        jtxtTax.setText(String.format("Rs. %.2f", cTax));
        jtxtSubTotal.setText(String.format("Rs. %.2f", cTotal1));
        jtxtTotal.setText(String.format("Rs. %.2f", cTotal1 + cTax));
    }

    public void Change() {
        double sum = 0;
        double tax = 3.9;
        double cash = Double.parseDouble(jtxtDisplay.getText());
        for (int i = 0; i < jTable1.getRowCount(); i++) {
            sum += Double.parseDouble(jTable1.getValueAt(i, 2).toString());
        }
        double cTax = (sum * tax) / 100;
        double cChange = cash - (sum + cTax);
        jtxtChange.setText(String.format("Rs. %.2f", cChange));
    }
