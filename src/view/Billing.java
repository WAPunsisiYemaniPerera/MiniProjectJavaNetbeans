    private void jbtnPayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnPayActionPerformed
        if (jcboPayment.getSelectedItem().equals("Cash")) {
            Change();
        } else {
            jtxtChange.setText("");
            jtxtDisplay.setText("");
        }
        saveBillingData();
    }//GEN-LAST:event_jbtnPayActionPerformed
   
    private void saveBillingData() {
        try {
            double subTotal = Double.parseDouble(jtxtSubTotal.getText().replace("Rs. ", ""));
            double tax = Double.parseDouble(jtxtTax.getText().replace("Rs. ", ""));
            double total = Double.parseDouble(jtxtTotal.getText().replace("Rs. ", ""));
            billingController.saveBillingData(subTotal, tax, total);
            JOptionPane.showMessageDialog(this, "Billing data saved successfully!");
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Error parsing billing data!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void addItemToTable(String itemName, double priceOfItem) {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.addRow(new Object[]{itemName, "1", priceOfItem});
        ItemCost();
    }
    
