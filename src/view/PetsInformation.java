    private void search() {
    String srch = searchbox.getText();
    try {
        String sql = "SELECT * FROM pets WHERE PetName LIKE '%" + srch + "%' OR PetID LIKE '%" + srch + "%'";
        Connection conn = petController.getConnections();  // Assuming you have a method to get DB connection
        PreparedStatement pst = conn.prepareStatement(sql);
        ResultSet rs = pst.executeQuery();
        jTable1.setModel(DbUtils.resultSetToTableModel(rs));
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, e);
    }
}
