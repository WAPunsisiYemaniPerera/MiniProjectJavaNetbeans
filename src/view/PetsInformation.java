    private void jbtnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnAddActionPerformed
        // Check if any of the fields are empty
        if(jtxtPetID.getText().isEmpty() || jtxtPetName.getText().isEmpty() ||
            jtxtType.getText().isEmpty() || jtxtAge.getText().isEmpty() ||
            jtxtOwnerName.getText().isEmpty() || jtxtTelephone.getText().isEmpty() ||
            jtxtAddress.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill in all fields.");
            return; // Exit the method if any field is empty
        }
    
    // If all fields are filled, proceed to add the pet record
            petController.addPet(jtxtPetID.getText(), jtxtPetName.getText(), jtxtType.getText(), jtxtAge.getText(),
            jtxtOwnerName.getText(), jtxtTelephone.getText(), jtxtAddress.getText());
            JOptionPane.showMessageDialog(this, "Record Added");
            updateTable();
            setNextPetID();
            resetFields();
    }//GEN-LAST:event_jbtnAddActionPerformed
    
    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();
        int selectedRow = jTable1.getSelectedRow();
        jtxtPetID.setText(tableModel.getValueAt(selectedRow, 1).toString());
        jtxtPetName.setText(tableModel.getValueAt(selectedRow, 2).toString());
        jtxtType.setText(tableModel.getValueAt(selectedRow, 3).toString());
        jtxtAge.setText(tableModel.getValueAt(selectedRow, 4).toString());
        jtxtOwnerName.setText(tableModel.getValueAt(selectedRow, 5).toString());
        jtxtTelephone.setText(tableModel.getValueAt(selectedRow, 6).toString());
        jtxtAddress.setText(tableModel.getValueAt(selectedRow, 7).toString());
    }//GEN-LAST:event_jTable1MouseClicked

    
    private void jbtnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnDeleteActionPerformed
        DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();
    int selectedRow = jTable1.getSelectedRow();
    
    if (selectedRow == -1) { // Check if no row is selected
        JOptionPane.showMessageDialog(this, "Please select a row to delete.");
        return; // Exit the method
    }

    int id = Integer.parseInt(tableModel.getValueAt(selectedRow, 0).toString());

    petController.deletePet(id);
    JOptionPane.showMessageDialog(this, "Record Deleted");
    updateTable();
    setNextPetID();
    resetFields();
    }//GEN-LAST:event_jbtnDeleteActionPerformed

